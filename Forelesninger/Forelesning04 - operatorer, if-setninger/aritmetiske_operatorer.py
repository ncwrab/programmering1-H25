
# Vi kan bruke aritmetiske operatorer direkte på tall
print( 4 * 7)

# Mer vanlig er det å lagre verdier i variabler for så å bruke variablenes data i videre utregninger
# Her opprettes to variabler med hver sin verdi.
num_1 = 4
num_2 = 7

# Under brukes variablenes verdi for å utføre enkel matematikk med de aritmetiske operatorene

answer = num_1 + num_2
print(f"Addisjon: {answer}")

answer = num_1 - num_2
print(f"Subtraksjon: {answer}")

answer = num_1 * num_2
print(f"Multiplikasjon: {answer}")

answer = num_1 / num_2
print(f"Divisjon: {answer}")

answer = num_1 % num_2
print(f"Modulus: {answer}")

answer = num_1 ** num_2
print(f"Eksponentiering: {answer}")

answer = num_1 // num_2
print(f"Floor division: {answer}")
