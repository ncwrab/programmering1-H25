# Opprette og bruke variabler

course_name = "Programmering 3"     # Oppretter en variabel med navnet course_name. Innholdet settes til "Programmering 3". Innholdet er av datatypen string, som kjennetegnes av "" rundt innholdet.
print(course_name)                  # Skriver ut innholdet i variabelen til konsollen

print("course_name")                # Her brukes ikke variabelen (dette blir en string fordi vi har " " rundt teksten)

course_name = "Programmering 1"     # Endrer/oppdaterer/overskriver innholdet i variabelen
print(course_name)                  # Bruker variabelen, med det nye innholdet

variabel = None                     # Definerer en tom variabel
print(variabel)
